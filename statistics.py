def get_contrib_hashes():
    # Returns SHA256 hashes of contributed (non-owner) repos
    query_count('get_contrib_hashes')
    hashes = set()
    query = '''
    query ($owner_affiliation: [RepositoryAffiliation], $login: String!, $cursor: String) {
        user(login: $login) {
            repositories(first: 100, after: $cursor, ownerAffiliations: $owner_affiliation) {
                edges {
                    node {
                        ... on Repository {
                            nameWithOwner
                        }
                    }
                }
                pageInfo {
                    endCursor
                    hasNextPage
                }
            }
        }
    }'''
    variables = {'owner_affiliation': ['COLLABORATOR', 'ORGANIZATION_MEMBER'], 'login': USER_NAME, 'cursor': None}
    while True:
        request = simple_request(get_contrib_hashes.__name__, query, variables)
        for edge in request.json()['data']['user']['repositories']['edges']:
            hashes.add(hashlib.sha256(edge['node']['nameWithOwner'].encode('utf-8')).hexdigest())
        if not request.json()['data']['user']['repositories']['pageInfo']['hasNextPage']:
            break
        variables['cursor'] = request.json()['data']['user']['repositories']['pageInfo']['endCursor']
    return hashes


def query_count(funct_id):
    global QUERY_COUNT
    QUERY_COUNT[funct_id] += 1


def perf_counter(funct, *args):
    start = time.perf_counter()
    funct_return = funct(*args)
    return funct_return, time.perf_counter() - start


def formatter(query_type, difference, funct_return=False, whitespace=0):
    print('{:<23}'.format('   ' + query_type + ':'), sep='', end='')
    print('{:>12}'.format('%.4f' % difference + ' s ')) if difference > 1 else print('{:>12}'.format('%.4f' % (difference * 1000) + ' ms'))
    if whitespace:
        return f"{'{:,}'.format(funct_return): <{whitespace}}"
    return funct_return


